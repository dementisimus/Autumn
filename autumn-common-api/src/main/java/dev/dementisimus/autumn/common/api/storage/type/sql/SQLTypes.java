package dev.dementisimus.autumn.common.api.storage.type.sql;

/**
 * All supported sql storage types
 *
 * @since 1.0.0
 */
public class SQLTypes {

    /**
     * Represents a Tinytext in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String TINYTEXT = "TINYTEXT";

    /**
     * Represents a Mediumtext in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String MEDIUMTEXT = "MEDIUMTEXT";

    /**
     * Represents a Longtext in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String LONGTEXT = "LONGTEXT";

    /**
     * Represents a Bit in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String BIT = "BIT";

    /**
     * Represents a Boolean in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String BOOLEAN = "BOOLEAN";

    /**
     * Represents an Integer in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String INTEGER = "INTEGER";

    /**
     * Represents a Float in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String FLOAT = "FLOAT";

    /**
     * Represents a Double in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String DOUBLE = "DOUBLE";

    /**
     * Represents a Date in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String DATE = "DATE";

    /**
     * Represents a Datetime in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String DATETIME = "DATETIME";

    /**
     * Represents a Timestamp in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String TIMESTAMP = "TIMESTAMP";

    /**
     * Represents a Time in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String TIME = "TIME";

    /**
     * Represents a Year in the SQL storage
     *
     * @since 1.0.0
     */
    public static final String YEAR = "YEAR";
}
